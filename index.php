					<p>A book that empowers you to explore your spiritual self and could well change your life. "More Than a Life" is the extraordinary story of Sadhguru – a young agnostic who turned yogi, a wild motorcyclist who turned mystic, a skeptic who turned spiritual guide. Pulsating with his razor-sharp intelligence, bracing wit and modern-day vocabulary, the book empowers you to explore your spiritual self and could well change your life. In his early years, Jaggi Vasudev (or Sadhguru as he is now known) was a chronic truant, a boisterous prankster, and later a lover of motorbikes and fast cars. It is evident that the same urgency, passion and vitality echo in his spiritual pursuits to this day, from his creation of the historic Dhyanalinga - the mission of three lifetimes - to his approach 	as a guru. <br><br>
					In Sadhguru's view, faith and reason, spirituality and science, the sacred and the material, cannot be divided into easy binaries. He sees people as "spiritual beings dabbling with the material rather than the reverse", and liberation as the fundamental longing in every form of life. Truth for him is a living experience instead of a destination, a conclusion, or a matter of metaphysical speculation. The possibility of self-realization, he strongly believes, is available to all. Drawing upon extended conversations with Sadhguru, interviews with Isha colleagues and fellow meditators, poet Arundhathi Subramaniam presents an evocative portrait of a contemporary mystic and guru – a man who seems to pack the intensity and adventure of several lifetimes into a single one.</p>
